package com.taylorsuniversity.models.bean;

/**
 * This bean class is used to set and retrieve the footer links properties 
 * configured in the footer component of the home-page template.
 *
 */
public class FooterModelLinkPathBean {
	
	private String linkPath;

	public String getLinkPath() {
		return linkPath;
	}

	public void setLinkPath(String linkPath) {
		this.linkPath = linkPath;
	}
	
}
